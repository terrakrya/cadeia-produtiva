<template>
  <b-modal :id="id" hide-footer title="Nota metodológica">
    METODOLOGIA DO INDICADOR DE PREÇOS DA CASTANHA-DA-AMAZÔNIA SIE-CASTANHA/OCA
    <br />

    <br />
    <h4>OBJETIVO</h4>
    <br />
    O presente documento descreve a metodologia de obtenção do Indicador de
    Preços da Castanha-da-Amazônia SIE-Castanha, com responsabilidade de
    execução pelo OCA (Observatório da Castanha-da-Amazônia).<br />
    <br />

    <br />
    <h4>DEFINIÇÕES</h4>
    <br />
    Indicador de Preços da Castanha-da-Amazônia: O Indicador de Preços da
    Castanha-da-Amazônia é uma média aritmética de cada natureza da fonte para
    cada um dos produtos cadastrados no sistema.<br />
    Natureza da Fonte: Tipo do preço coletado e registrado no sistema, sendo
    “Transação Realizada” ou “Preço de Mercado”.<br />
    - Transação Realizada: Preço referente à uma comercialização especifica
    realizada em determinado município, onde deve ser informado o volume total
    da transação.<br />
    - Preço de Mercado: Preço referente à uma pesquisa sobre o preço praticado
    em determinado município, sem a necessidade da realização da compra. Não é
    necessário informar o volume para esta natureza da fonte.<br />
    SIE-Castanha: Sistema de Informações Econômicas da Castanha-da-Amazônia.<br />
    <br />
    Produtos: Os produtos que serão cadastrados no sistema devem seguir a
    seguinte lógica de organização: Espécie, Grupo, Industrialização, Tamanho e
    Qualidade.<br />
    - Espécie: Espécie do produto que os preços serão coletados. Para a
    Castanha-da-Amazônia, temos o nome cientifico “Bertholletia excelsa”.<br />
    - Grupo: Agrupamento dos produtos derivados de terminada espécie. Para a
    Castanha-da-Amazônia temos os seguintes grupos: Com Casca, Sem Casca e
    Derivados. - Industrialização: Etapa do processo de beneficiamento do
    produto, onde temos os seguintes valores para a Castanha-da-Amazônia: In
    Natura, Seca (Dry), Desidratada, Farinha e Óleo.<br />
    - Tamanho: Tamanho do produto em que os preços foram obtidos, podendo variar
    entre os seguintes valores: Granulada, Quebrada, Ferida, Miúda, Pequena,
    Média, Grande, Extra Grande e N/A (Não Aplicável).<br />
    - Qualidade: Qualidade do produto que teve os preços obtidos, variando entre
    as seguintes opções: Tradicional (Basic), Boa (Good) e Ótima (Premium).<br />
    Organização: Entidade pertencente a cadeia produtiva que irá agregar
    informantes para a obtenção dos preços praticados da Castanha-da-Amazônia.
    Podendo ser uma associação, cooperativa, beneficiadora, indústria, mercado,
    etc.<br />
    Informantes: Pessoa pertencente à uma determinada organização que irá
    informar o preço praticado em determinado município. Os informantes podem
    ser extrativistas, intermediários, comerciantes, gestores, etc.<br />
    Volume da Compra: Caso o preço informado tenha como natureza da fonte uma
    “Transação Realizada” é necessário informar o peso na unidade de medida
    registrada do usuário e armazenada no sistema em quilogramas (Kg) por meio
    da conversão das medidas, quando necessário.
    <br />

    <br />
    <h4>Conversão de Medidas de Volume:</h4>
    <br />
    Para a conversão entre as medidas de volume dos registros de preços de
    transações realizada, utilizar os seguintes valores: - 1 Lata/Latão = 11
    Kg;<br />
    - 1 Caixa = 24 Kg;<br />
    - Hectolitro = 55 Kg;<br />
    - Saca = 60 Kg;<br />
    - Barrica = 72 Kg;<br />
    - Tonelada = 1000 Kg. <br />
    Preço Mínimo: Valor relatado por um informante do sistema referente ao menor
    preço quando a natureza da fonte for “Preço de Mercado” ou referente ao
    preço da comercialização quando a natureza da fonte for “Transação
    Realizada”.<br />
    Preço Máximo: Valor relatado por um informante do sistema referente ao maior
    preço quando a natureza da fonte for “Preço de Mercado” ou referente ao
    preço da comercialização (igual ao preço mínimo) quando a natureza da fonte
    for “Transação Realizada”. <br />
    Região de Referência: Município e Estado onde foi realizada a obtenção do
    preço da castanha-da-amazônia.<br />
    Regiões imediatas de Comercialização: Agrupamentos dos municípios que
    concentram a produção e distribuição do produto registrado. As regiões
    imediatas deverão ser determinadas com base na análise dos dados registrado
    no sistema. Moeda: Tipos de moedas disponíveis para informar o preço obtido
    da Castanha-da-Amazônia, podendo ser: Reais (R$), Dólar (US$) e Euro (€).<br />
    <br />

    <br />
    <h4>DESCRIÇÃO DO PROCESSO</h4>
    <br />
    Neste capitulo serão apresentadas informações sobre o universo da pesquisa,
    como as regiões de coleta dos preços semanais e seus informantes, a apuração
    do indicador, desde o início da coleta até o cálculo do indicador. Por fim é
    apresentado o processo de divulgação do Indicador. sa
    <br />
    <br />
    <h4>Universo da Pesqui</h4>
    <br />
    <h4>Região de Referência</h4>
    <br />
    A região de referência deste indicador são todos os estados, e seus
    municípios, produtores da Castanha-da-Amazônia, conforme divulgado pelo
    IBGE, sendo os seguintes estados: Acre, Amapá, Amazonas, Mato Grosso, Pará,
    Rondônia e Roraima. Para o melhor entendimento da cadeia produtiva e do
    escoamento da produção, agrupar diferentes municípios que possuam
    similaridades em regiões imediatas de comercialização.

    <br />
    <h4>Seleção e Avaliação dos Informantes</h4>
    <br />
    Para a seleção dos informantes deve se considerar agentes pertencentes a
    cadeia produtiva, como extrativistas, atravessadores, comerciantes, gestores
    e colaboradores de cooperativas ou industrias de beneficiamento da
    Castanha-da-Amazônia. A inclusão de novos informantes deve ser feita: a) a
    partir de manifestação voluntária do agente, b) por indicação da OCA, c) por
    indicação de agentes já colaboradores do sistema pertencentes a outras
    organizações da cadeia produtiva.<br />
    A decisão sobre a participação de um novo informante deve levar em
    consideração os seguintes pontos:<br />
    - A representatividade do potencial informante em sua região e nos negócios
    do setor;<br />
    - O detalhamento e a qualidade das informações prestadas para o cadastro
    inicial;<br />
    - A localização geográfica de sua atuação;<br />
    - A credibilidade do informante, considerando seu tempo de atuação no
    mercado, informações levantadas em websites e obtidas por outros informantes
    da mesma região;<br />
    - A frequência que realiza negócios ou é informado de negócios envolvendo
    produtos cadastrados no sistema;<br />
    - Interesse especifico do projeto em aumentar o numero de informantes em
    determinado município para elevar a quantidade amostral.<br />
    Caso seja aprovado na avaliação realizada pela equipe da OCA e se disponha a
    relatar informações de forma periódica, passará a ser considerado um
    informante do indicador de preços da Castanha-da-Amazônia. Para permanecer
    como informante, o colaborador deve ser considerado efetivo. Para isto, é
    considerado os seguintes pontos:<br />
    - Frequência em que o colaborador atende aos contatos da Equipe OCA;<br />
    - Frequência em que o colaborador relata no sistema os preços de
    comercialização ou pesquisa de mercado;<br />
    - Frequência com que, tendo relatado valores com erros e foram excluídos dos
    cálculos, não apresentou justificativa plausível para ocorrência do valor
    informado.<br />

    <br />
    <h4>Apuração do Indicador</h4>
    <br />
    Com a definição dos prontos principais do indicador e do universo da
    pesquisa, são apresentadas as etapas de operação do Indicador no cotidiano.

    <br />
    <h4>Obtenção dos Dados</h4>
    <br />
    Semanalmente, os informantes do sistema irão imputar as informações
    coletadas durantes uma semana sobre as transações realizadas e as pesquisas
    de mercado dos produtos cadastrados no sistema. As informações necessárias
    que são cadastradas no registro da coleta de preço são:<br />
    - Organização que o informante pertence;<br />
    - Nome do informante;<br />
    - Natureza da Fonte;<br />
    - Quantidade da transação;<br />
    - Preço mínimo e preço máximo;<br />
    - A unidade de medida;<br />
    - O produto;<br />
    - Data de comercialização ou da pesquisa de mercado;<br />
    - Estado, Cidade e País;<br />
    - Moeda utilizada na comercialização;<br />
    Os dados apurados são registrados e armazenados no Sistema de Informações
    Econômicas da Castanha-da-Amazônia para o cálculo do Indicador do Preço da
    Castanha semanal.

    <br />
    <h4>Tratamento Inicial dos Preços Coletados</h4>
    <br />
    Os preços relatados por agentes colaboradores devem ser relativizados de
    acordo sua posição geográfica. A distância e o valor de frete deve ser
    considerado na ponderação médio em diferentes regiões. O valor do frete será
    determinado pela distância e condições logísticas do principal centro de
    consumo (Mercado de São Paulo). Esta análise deve ser feita por um estudo
    específico e atualizada anualmente.

    <br />
    <h4>Obtenção da Média dos Preços Semanais</h4>
    <br />
    Com os dados dos preços registrados no SIE-Castanha, é feito uma média
    aritmética para cada um dos produtos cadastrados com todos os valores que
    ocorreram durante uma determinada semana afim de descobrir o preço médio
    daquela determinada semana.

    <br />
    <h4>Tratamentos Estatísticos</h4>
    <br />
    <h4>Ponderação logística</h4>
    <br />
    Os preços relatados por agentes colaboradores devem ser relativizados de
    acordo sua posição geográfica. A distância e o valor de frete deve ser
    considerado na ponderação médio em diferentes regiões. O valor do frete será
    determinado pela distância e condições logísticas do principal centro de
    consumo (Mercado de São Paulo). Esta análise deve ser feita por um estudo
    específico e atualizada anualmente.

    <br />
    <h4>Atualização à valor presente</h4>
    <br />
    Para proporcionar uma melhor analise do historico dos preços de produtos
    registrados é importante disponibilizar a opção da visualização com os
    valores atualizados à valor presente, de acordo com o IPCA (Índice Nacional
    de Preços ao Consumidor Amplo).

    <br />
    <h4>Exclusão de valores críticos por meio do coeficiente de variação</h4>
    <br />
    Registros de preços que estiverem com valores em grande discrepância com os
    restantes da amostra devem ser descartados, por meio da identificação do
    coeficiente de variação padrão de determinada média de preço e da criação de
    uma relação deste coeficiente com o valor crítico para identificar quais
    valores críticos devem ser excluídos.

    <br />
    <br />
    <h4>Divulgação do Indicador</h4>
    <br />
    O Indicador de Preços da Castanha-da-Amazônia é divulgado e atualizado em
    série histórica semanalmente na homepage do SIE-Castanha de endereço
    eletrônico: “”. Durante a safra, a equipe da OCA deverá divulgar boletins
    mensais de analise do comportamento dos preços praticados no mercado para os
    diferentes produtos cadastrados no SIE-Castanha.
